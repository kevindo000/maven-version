    private void __enable_delete_pop_up_panel__(javax.swing.JPanel panel) {
        if (panel == this.task_page_task_delete_pane) {
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_task_delete_pane)) component.setEnabled(true);
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_subtask_delete_pane)) component.setEnabled(false);
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_task_category_delete_pane)) component.setEnabled(false);
        } else if (panel == this.task_page_subtask_delete_pane) {
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_task_delete_pane)) component.setEnabled(false);
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_subtask_delete_pane)) component.setEnabled(true);
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_task_category_delete_pane)) component.setEnabled(false);
        } else if (panel == this.task_page_task_category_delete_pane) {
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_task_delete_pane)) component.setEnabled(false);
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_subtask_delete_pane)) component.setEnabled(false);
            for (java.awt.Component component: GeneralUIFunctions.getAllComponents(this.task_page_task_category_delete_pane)) component.setEnabled(true);
        } else
            System.out.println("ERROR: Panel " + panel + " does not belong to the delete pop up window!");
    }

